/// Count the number of syllables in a word.
///
/// Uses dictionary lookup first, then falls back to vowel counting.
int countSyllablesInWord(String word) {
  if (word.isEmpty) return 0;

  final cleaned = word.toLowerCase().replaceAll(RegExp(r"[^a-z']"), '');
  if (cleaned.isEmpty) return 0;

  // Check dictionary first
  final dictCount = _dictionary[cleaned];
  if (dictCount != null) return dictCount;

  // Handle contractions
  final contractionCount = _countContraction(cleaned);
  if (contractionCount != null) return contractionCount;

  // Fall back to vowel counting algorithm
  return _countByVowels(cleaned);
}

/// Count syllables in a line of text.
int countSyllablesInLine(String line) {
  if (line.trim().isEmpty) return 0;

  final words = line.split(RegExp(r'\s+'));
  int total = 0;
  for (final word in words) {
    total += countSyllablesInWord(word);
  }
  return total;
}

/// Count syllables in each line of a haiku.
///
/// Returns a list of syllable counts, one per line.
List<int> countHaikuSyllables(String text) {
  final lines = text.split('\n');
  return lines.map(countSyllablesInLine).toList();
}

/// Check if the haiku follows the 5-7-5 pattern.
bool isValidHaikuStructure(String text) {
  final counts = countHaikuSyllables(text);
  if (counts.length != 3) return false;
  return counts[0] == 5 && counts[1] == 7 && counts[2] == 5;
}

