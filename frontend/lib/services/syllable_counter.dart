/// Count syllables using vowel patterns.
///
/// This is the fallback algorithm when a word isn't in the dictionary.
int _countByVowels(String word) {
  if (word.isEmpty) return 0;

  // Vowel pattern
  final vowels = RegExp(r'[aeiouy]+', caseSensitive: false);
  final matches = vowels.allMatches(word);
  int count = matches.length;

  // Adjust for silent 'e' at end
  if (word.endsWith('e') && !word.endsWith('le')) {
    // Check if 'e' follows a consonant (silent e)
    if (word.length > 1) {
      final beforeE = word[word.length - 2];
      if (!'aeiouy'.contains(beforeE)) {
        count = count > 1 ? count - 1 : count;
      }
    }
  }

  // Words ending in 'le' after consonant add a syllable
  if (word.length > 2 && word.endsWith('le')) {
    final beforeLe = word[word.length - 3];
    if (!'aeiouy'.contains(beforeLe)) {
      // 'le' is its own syllable - already counted by vowel match
    }
  }

  // Adjust for common suffixes that are syllabic
  if (word.endsWith('ed')) {
    final beforeEd = word.length > 2 ? word[word.length - 3] : '';
    // 'ed' is silent after most consonants except t/d
    if (beforeEd == 't' || beforeEd == 'd') {
      // 'ed' is pronounced as separate syllable
      // Already counted
    }
  }

  // Handle 'tion', 'sion' as single syllable (already matched as 'io')
  // Handle 'eous', 'ious' patterns
  if (word.contains('eous') || word.contains('ious')) {
    count = count > 1 ? count - 1 : count;
  }

  // Ensure at least 1 syllable for any word
  return count > 0 ? count : 1;
}

